public class Array{
    public static void main(String[] args) {
        //Definition:
        //  ARRAYS are collection of data which is inside single variable
        /*
        * SYNTAX
        *   datatype[] name = new datatype[size]
        *
        * Ex:
        *   int[] arr = new int[10];
        *   String names = new String[20];
        *
        *   By Default 0 is Stored inside an empty Array
        *   "arr" is reference variable and actual data's of Array are stored in Heap Memory
        *
        *   DATA'S or VALUES which are stored in array are not stored in contiguous memory location (DEPENDS ON JVM)
        *
        *   int[] arr; COMPILE TIME               // DECLARATION
        *   new int[10]; RUNTIME TIME             //INITIALISATION
        *
        * DECLARATION:
        *      During Declaration "arr" ref variable is defined in STACK
        * INITIALISATION:
        *       During Initialisation to Object is being created in HEAP
        *       DYNAMIC MEMORY ALLOCATION
        *       The memory space is allocated during execution time or the run time
        * */
    }
}